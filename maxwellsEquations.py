import math

def maxwellsEquation():
    # get the x value from the user
    x = float(input("Enter the x value: "))
    # get the y value from the user
    y = float(input("Enter the y value: "))
    # get the z value from the user
    z = float(input("Enter the z value: "))
    # calculate maxwells equation
    maxwellsEquation = (x**2)+(y**2)+(z**2)
    # print maxwells equation
    print("Maxwells equation is: ", maxwellsEquation)